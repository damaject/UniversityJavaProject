package first;

public class Vector {

    private double x;
    private double y;
    private double z;
    private int x1, y1, z1;

    public Vector(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Vector(int x, int y, int z) {
        this.x1 = x;
        this.y1 = y;
        this.z1 = z;
    }
}
